package chap01.exam01;

public class Variable {

	public static void main(String[] args) {
		
		boolean varBool = true;
		String varString = "문자열"; // 문자열 : 여러 문자
		char varChar = 'A'; // 문자 : 한글자만 들어가야 하며 싱글쿼터를 사용 한다.
		
		int varInt = 0; // 정수타입(급여정도)
		long varLong = 10000; // 큰 수(연봉정도)
		
		float varFloat = 0.01f; // 작은 소숫점(f  추가 필요)
		double varDouble = 0.000001; // 큰 소숫점(소숫점 5자리 이상)
		

	}	

}
