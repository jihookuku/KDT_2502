package chap09.exam04;

public class Employee <A,B,C,D,E,F,G,H>{
	
	private A no;//사원번호 : no
	private B name;//이름 : name
	private C age;//나이 : age
	private D salary;//급여 : salary
	private E assets;//자산  : assets
	private F commission;//성과급 : commission
	private G married;//결혼유무 : married
	private H hobby;//취미 : hobby
	
	public A getNo() {
		return no;
	}
	public void setNo(A no) {
		this.no = no;
	}
	public B getName() {
		return name;
	}
	public void setName(B name) {
		this.name = name;
	}
	public C getAge() {
		return age;
	}
	public void setAge(C age) {
		this.age = age;
	}
	public D getSalary() {
		return salary;
	}
	public void setSalary(D salary) {
		this.salary = salary;
	}
	public E getAssets() {
		return assets;
	}
	public void setAssets(E assets) {
		this.assets = assets;
	}
	public F getCommission() {
		return commission;
	}
	public void setCommission(F commission) {
		this.commission = commission;
	}
	public G getMarried() {
		return married;
	}
	public void setMarried(G married) {
		this.married = married;
	}
	public H getHobby() {
		return hobby;
	}
	public void setHobby(H hobby) {
		this.hobby = hobby;
	}
	
	

}
